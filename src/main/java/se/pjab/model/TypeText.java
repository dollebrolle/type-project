package se.pjab.model;

public class TypeText {

	private long id;
	private String content;
	
	public TypeText() {
		
	}

	public TypeText(long id, String content) {
		super();
		this.id = id;
		this.content = content;
	}

	public long getId() {
		return id;
	}

	public String getContent() {
		return content;
	}
	
	
	
}
